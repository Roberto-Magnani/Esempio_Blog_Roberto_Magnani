            <h2>Classi</h2>
            <br>
            <p>La classe è una parte fondamentale dell'identità e della natura dei personaggi nel gioco di ruolo
                Dungeons & Dragons. Le capacità, i punti di forza e i punti deboli di un personaggio sono in gran
                parte definiti dalla sua classe; scegliere una classe è uno dei primi passi che un giocatore compie
                per creare un personaggio giocante di Dungeons & Dragons. La classe di un personaggio ne influenza
                le abilità e le caratteristiche. Un party di personaggi completo richiede una varietà di
                caratteristiche offerte dalle classi trovate all'interno del gioco.</p>

            <p>Dungeons & Dragons è stato il primo gioco a introdurre l'uso delle classi di personaggi nel gioco di
                ruolo. In seguito, anche molti altri giochi di ruolo tradizionali e giochi di ruolo online hanno
                adottato il concetto. Le classi di Dungeons & Dragons sono state definite nel Manuale del Giocatore,
                uno dei tre manuali fondamentali; una varietà di classi alternative sono state definite anche in
                manuali supplementari.
            </p>
            <p>La maggior parte delle edizioni di Dungeons & Dragons consente la possibilità di avanzare
                contemporaneamente in più di una classe, acquisendo livelli alternativamente da diverse classi, con
                un meccanismo noto come "multiclasse"...</p>

